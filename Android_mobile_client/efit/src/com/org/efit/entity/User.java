package com.org.efit.entity;

public class User {
private Integer userId;
private String username;
private String password;
private String headAdreess;
private String sex;
private String t_size;
private String d_size;
public Integer getUserId() {
	return userId;
}
public void setUserId(Integer userId) {
	this.userId = userId;
}
public String getUsername() {
	return username;
}
public void setUsername(String username) {
	this.username = username;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getHeadAdreess() {
	return headAdreess;
}
public void setHeadAdreess(String headAdreess) {
	this.headAdreess = headAdreess;
}
public String getSex() {
	return sex;
}
public void setSex(String sex) {
	this.sex = sex;
}
public String getT_size() {
	return t_size;
}
public void setT_size(String t_size) {
	this.t_size = t_size;
}
public String getD_size() {
	return d_size;
}
public void setD_size(String d_size) {
	this.d_size = d_size;
}

}
